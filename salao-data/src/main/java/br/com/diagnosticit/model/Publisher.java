package br.com.diagnosticit.model;

/**
 *
 * @author cristiano
 */
public class Publisher {
    
    private Long id;
    private String name;
    private String lastName;

    public Publisher() {
        
    }

    public Publisher(String name, String lastName) {
        this.name = name;
        this.lastName = lastName;
    }

    public Publisher(Long id, String name, String lastName) {
        this.id = id;
        this.name = name;
        this.lastName = lastName;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }
    
    
}
